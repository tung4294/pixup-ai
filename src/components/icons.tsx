export const Icon: React.FC<IconProps> = ({ name, className = "w-6 h-6", ...props }) => {
  const icon = icons[name];
  if (!icon) return null;
  return React.cloneElement(icon, { className, ...props } as React.SVGProps<SVGSVGElement>);
};
